public class MathUtil {

    public static double topla(double a, double b) { // single reposnsibility
        return a + b;
    }

    public static double cix(double a, double b) {
        return a - b;
    }

    public static double vur(double a, double b) {
        return a * b;
    }

    public static double bol(double a, double b) {
        return a / b;
    }

    public static double faiz(double a, double b) {
        return a * b / 100;
    }
}
